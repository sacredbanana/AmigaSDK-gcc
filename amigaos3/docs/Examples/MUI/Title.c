static ULONG Register_NEW(struct IClass *cl, Object *obj, Msg msg)
{
	Object *positionCycle;
	Object *newableCheckmark;
	Object *closableCheckmark;
	Object *addButton;
	Object *dragDest;
	Object *reg;
	Object *titles;

	if((obj = DoSuperNew(cl, obj,
		MUIA_Group_Horiz, FALSE,

		Child, HGroup,
			Child, LLabel("Position:"),
			Child, positionCycle = Cycle(titlePositions),
			End,

		Child, HGroup,
			Child, newableCheckmark = CheckMark(FALSE),
			Child, Label("New button"),
			Child, HSpace(0),
			End,

		Child, HGroup,
			Child, closableCheckmark = CheckMark(FALSE),
			Child, Label("Close button"),
			Child, HSpace(0),
			End,

		Child, HGroup,
			MUIA_Group_SameSize, TRUE,
			Child, addButton = KeyButton("add new tab", 'a'),
			Child, dragDest = NewObject(drop_mcc->mcc_Class,NULL,TAG_DONE),
			End,

		Child, reg = VGroup,
			Child, titles = NewObject(titles_mcc->mcc_Class, NULL, TAG_DONE),
			End,

		TAG_MORE, inittags(msg))) != NULL)
	{
		ULONG i;

		DoMethod(positionCycle, MUIM_Notify, MUIA_Cycle_Active, MUIV_EveryTime, titles, 3, MUIM_Set, MUIA_Title_Position, MUIV_TriggerValue);
		DoMethod(newableCheckmark, MUIM_Notify, MUIA_Selected, MUIV_EveryTime, titles, 3, MUIM_Set, MUIA_Title_Newable, MUIV_TriggerValue);
		DoMethod(closableCheckmark, MUIM_Notify, MUIA_Selected, MUIV_EveryTime, titles, 3, MUIM_Set, MUIA_Title_Closable, MUIV_TriggerValue);
		DoMethod(addButton, MUIM_Notify, MUIA_Pressed, FALSE, titles, 1, MUIM_Title_New);

		// add some initial tabs
		for(i = 0; i < 3; i++)
			DoMethod(titles, MUIM_Title_New);
		set(reg, MUIA_Group_ActivePage, MUIV_Group_ActivePage_First);
	}

	return (ULONG)obj;
}

DISPATCHER(Register_Dispatcher)
{
	switch(msg->MethodID)
	{
		case OM_NEW					: return Register_NEW(cl, obj, (APTR)msg);
	}

	return DoSuperMethodA(cl, obj, msg);
}

